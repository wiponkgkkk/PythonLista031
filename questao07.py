#7) Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números

#Inputs

num1 = int(input("Informe um número " ))
num2 = int(input("Informe um número " ))
num3 = int(input("Informe um número " ))

#Cálculos

media = ((num1 + num2 + num3)//3)

#Prints

print(f"O valor da Média Aritmética desses números é {media}")